function rk1( t, y, f, h )

    q1 = f( t, y )

    return y + h * q1

end

###############################################

function rk2( t, y, f, h )

    q1 = f( t        , y              )
    q1 = f( t + h / 2, y + h / 2 * q1 )

    return y + h * q1

end

###############################################

function rk3( t, y, f, h )

    q1 = f( t            , y                  )
    q2 = f( t + h / 3    , y + h / 3 * q1     )
    q2 = f( t + 2 * h / 3, y + 2 * h / 3 * q2 )

    return y + h / 4 * ( q1 + 3 * q2 )

end

###############################################

function rk4( t, y, f, h )

    q1 = f( t        , y              )
    q2 = f( t + h / 2, y + h / 2 * q1 )
    q3 = f( t + h / 2, y + h / 2 * q2 )
    q4 = f( t + h    , y + h * q3     )

    return y + h/6 * ( q1 + 2 * q2 + 2 * q3 + q4 )

end

