function ab3!(t, U1, U2, U3, odefun!, dt, q1, q2, q3)

    U = U3 .+ dt ./ 12 .* (23 .* odefun!(t, U3, q3) .-
        16 .* odefun!(t-dt, U2, q2) .+ 5 .* odefun!(t-2*dt, U1, q1))

    U1 = U2
    U2 = U3
    U3 = U

    return U1, U2, U3

end

